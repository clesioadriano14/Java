    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed

        String email, senha;
        email = (String) txtEmail.getText();
        senha = String.valueOf(txtSenha.getPassword());
        if (email.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!", "Campos inv√°lidos", JOptionPane.WARNING_MESSAGE);
        } else {
            DAOUsuario dao = new DAOUsuario();
            if (dao.verificarLogin(email, senha)) {
                JOptionPane.showMessageDialog(null, "Login bem-sucedido", "", JOptionPane.PLAIN_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Email ou senha incorretos", "Falha no Login", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEntrarActionPerformed
    

